#ifndef KANZENBOARD_H_INCLUDED
#define KANZENBOARD_H_INCLUDED

//i行j列目(i>=0, j>=0)をマスi * 6 + jとおく.
//用意するメソッド：終了条件(2種類), 動かす
#pragma once
#include <string>
#include <algorithm>
#include "MoveCommand.h"

namespace kbb {
	int weight1 = 1000;		//青駒の個数の評価の重み(1〜1000)
	int weight2 = 1;		//駒の位置の評価の重み(1〜1000)
	int bitCountTable[1 << 18];	//bitCountTable[s] = sの2進数表記における桁の和
	int _myGoalDist[1 << 18];	//_myGoalDist[s] = (sのiビット目が1⇔マスiに駒がある)ときのゴールまでのマンハッタン距離の和.
	int _yourGoalDist[1 << 18];
	
	//xは0〜2^36 - 1
	inline int bitCount(long long x) {
		return bitCountTable[x & 262143] + bitCountTable[x >> 18];
	}
	
	//前処理. AI.cppで呼び出す.
	void prepare() {
		int i, j;
		
		for (i = 0; i < (1 << 9); i++) {
			int cnt = 0;
			for (j = 0; j < 9; j++) {
				if ((i >> j) & 1) cnt++;
			}
			bitCountTable[i] = cnt;
		}
		for (i = (1 << 9); i < (1 << 18); i++) {
			bitCountTable[i] = bitCountTable[i & 511] + bitCountTable[i >> 9];
		}
		
		for (i = 0; i < (1 << 18); i++) {
			_myGoalDist[i] = 0;
			_yourGoalDist[i] = 0;
			for (j = 0; j < 18; j++) {
				if ((i >> j) % 2 == 0) continue;
				//マスjからゴールまでのマンハッタン距離
				//相手陣はy = 0側に, 自陣はy = 5側にあるとする。
				int y = j / 6;
				int x = j % 6;
				int dist1 = y + std::min(x, 5 - x);
				int dist2 = 5 - y + std::min(x, 5 - x);
				_myGoalDist[i] += dist1;
				_yourGoalDist[i] += dist2;
			}
		}
	}
	
	//s = 0〜2^36 - 1. (s >> i) & 1 == 1 ⇔ マスiに自駒がある
	inline int myGoalDist(long long s) {
		return _myGoalDist[s & 262143] + _myGoalDist[s >> 18] + 3 * bitCountTable[s >> 18];
	}
	
	//s = 0〜2^36 - 1. (s >> i) & 1 == 1 ⇔ マスiに相手駒がある
	inline int yourGoalDist(long long s) {
		return _yourGoalDist[s & 262143] + _yourGoalDist[s >> 18] - 3 * bitCountTable[s >> 18];
	}
}

struct KanzenBoard
{
	long long existR;	//マスiに自分の赤がある ⇔ (existR >> i) & 1 == 1とする。
	long long existB;
	long long existr;	//マスiに敵の赤駒がある ⇔ (existr >> i) & 1 == 1とする。
	long long existb;
	
	//board[i]…マスiにある駒の種類(R, B, u)
	void toBitBoard(string board) {
		existR = existB = existr = existb = 0;
		for (int i = 0; i < 36; i++) {
			if (board[i] == 'R') { existR |= (1LL << i); }
			if (board[i] == 'B') { existB |= (1LL << i); }
			if (board[i] == 'r') { existr |= (1LL << i); }
			if (board[i] == 'b') { existb |= (1LL << i); }
		}
	}
	
	//手の生成. 手の個数を返す(teban=0 : 自分手番, teban=1:敵手番). from[], to[]に手を格納. (from, toはサイズ32以上の配列）
	//kiki[5 * i + j] = マスiと隣接するj番目のマスの番号. なければ-1。
	int makeMoves(int teban, int kiki[], int from[], int to[]) {
		int pos, i, cnt = 0;
		for (pos = 0; pos < 36; pos++) {
			if (teban == 0 && !(((existR | existB) >> pos) & 1)) continue;
			if (teban == 1 && !(((existr | existb) >> pos) & 1)) continue;
			for (i = pos * 5; kiki[i] != -1; i++) {
				int npos = kiki[i];
				//マスpos -> マスnposと駒を動かせるか？（自駒とぶつからないか）
				if (teban == 0 && (((existR | existB) >> npos) & 1)) continue;
				if (teban == 1 && (((existr | existb) >> npos) & 1)) continue;
				//動かせる
				from[cnt] = pos;
				to[cnt] = npos;
				cnt++;
			}
		}
		return cnt;
	}
	
	//先手の赤を動かした後の状態に更新する。
	inline void moveR(int from, int to) {
		existR &= ~(1LL << from);
		existR |= (1LL << to);
		existr &= ~(1LL << to);
		existb &= ~(1LL << to);
	}
	
	inline void moveB(int from, int to) {
		existB &= ~(1LL << from);
		existB |= (1LL << to);
		existr &= ~(1LL << to);
		existb &= ~(1LL << to);
	}
	
	inline void mover(int from, int to) {
		existr &= ~(1LL << from);
		existr |= (1LL << to);
		existR &= ~(1LL << to);
		existB &= ~(1LL << to);
	}
	
	inline void moveb(int from, int to) {
		existb &= ~(1LL << from);
		existb |= (1LL << to);
		existR &= ~(1LL << to);
		existB &= ~(1LL << to);
	}
	
	void move(int from, int to) {
		if ((existR >> from) & 1)
			moveR(from, to);
		else if ((existB >> from) & 1)
			moveB(from, to);
		else if ((existr >> from) & 1)
			mover(from, to);
		else if ((existb >> from) & 1)
			moveb(from, to);
		else
			assert(0);
	}
	
	//どちらが勝ち状態かを返す. (0…自分, 1…敵, 2…不明). 
	//teban … 0なら自分手番. 1なら敵手番. (手番 … 手を打つ直前のプレイヤー)
	int getWinPlayer(int teban) {
		if (existR == 0 || existb == 0) return 0;
		if (existB == 0 || existr == 0) return 1;
		if (teban == 0 && ((existB & 1LL) || ((existB >> 5) & 1LL))) return 0;
		if (teban == 1 && (((existb >> 30) & 1LL) || ((existb >> 35) & 1LL))) return 1;
		return 2;
	}
	
	//tebanプレイヤーが1手で脱出できるか？ (tebanが0なら自分手番)
	//・できない…MoveCommand(-1, -1, -1)
	//・できる  …動かし方（MoveCommand)を返す
	//深さ0での判定では, こちらを用いる. デバッグ用として, 相手番でも利用可能にする.
	MoveCommand getEscapeCommand(int teban) {
		if (teban == 0) {
			if (existB & 1LL) return MoveCommand(0, 0, 3);
			if ((existB >> 5) & 1LL) return MoveCommand(0, 5, 1);
			return MoveCommand(-1, -1, -1);
		}
		else {
			if ((existb >> 30) & 1LL) return MoveCommand(5, 0, 3);
			if ((existb >> 35) & 1LL) return MoveCommand(5, 5, 1);
			return MoveCommand(-1, -1, -1);
		}
	}
	
	//評価関数. tebanプレイヤーの有利さを返す. teban=0…自分手番.
	int evaluate(int teban) {
		int s0 = kbb::weight1 * kbb::bitCount(existB) - kbb::weight2 * kbb::myGoalDist(existB | existR);
		int s1 = kbb::weight1 * kbb::bitCount(existb) - kbb::weight2 * kbb::yourGoalDist(existb | existr);
		if (teban == 0) return s0 - s1;
		return s1 - s0;
	}
	
	//デバッグ用
	void printBoard() {
		for (int y = 0; y < 6; y++) {
			for (int x = 0; x < 6; x++) {
				int id = y * 6 + x;
				if ((existR >> id) & 1) std::cout << "R";
				else if ((existB >> id) & 1) std::cout << "B";
				else if ((existr >> id) & 1) std::cout << "r";
				else if ((existb >> id) & 1) std::cout << "b";
				else std::cout << ".";
			}
			std::cout << std::endl;
		}
		std::cout << std::endl;
	}
};
#endif